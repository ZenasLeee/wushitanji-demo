"""
解析层 — DeepSeek V4-Flash 结构化解析 + 菜名归一化
====================================================

从吾食谈记主项目 eval/score.py 同源裁剪而来：只保留部署期实际要用的
parse()（真实解析调用）与 normalize_dish()（同义词归一化），去掉了
评测专用的三层评分函数（score_l1/l2/l3）、用例加载、bad case 收集等
只在离线评测 harness 里用的部分。

冻结 prompt，不在 demo 里改字。
"""

import json
import os

from openai import OpenAI
from dotenv import load_dotenv

load_dotenv(os.path.join(os.path.dirname(__file__), ".env"))

client = OpenAI(
    api_key=os.environ["DEEPSEEK_API_KEY"],
    base_url="https://api.deepseek.com",
)

# 同义词归一化映射表
SYNONYM_MAP = {
    "酥肉": "小酥肉",
    "河间驴肉火烧": "驴肉火烧",
    "漳州四果汤": "四果汤",
    "柳州螺蛳粉": "螺蛳粉",
    "南昌拌粉": "拌粉",
    "洋芋焖饭": "土豆焖饭",
    "地瓜稀饭": "红薯稀饭",
}

SYSTEM_PROMPT = """你是饮食记录解析器。把用户一句话的吃饭记录解析成结构化 JSON。只输出 JSON,不要任何解释、前言或 markdown 代码块。

# 输出格式
{"l1": [菜品规范名, ...], "l2": {菜品: 份量量词}, "l3": {菜品: 油量描述}}
l2、l3 里的菜名必须和 l1 完全逐字一致。

# L1 菜品
- 列出用户实际吃的每一道菜,用最常见规范名(写"番茄炒蛋",不写"西红柿炒鸡蛋")。
- 一道菜就是一道,不拆成食材:"冬瓜排骨汤""木须肉""紫菜蛋汤"各算一道,不要拆成冬瓜+排骨、木耳+蛋+肉。
- 汤、粥、饮品各算独立一道菜。
- 蘸料、调味料不单列。
- 明显错别字纠正成规范名(红暑→红薯、炸耦盒→炸藕盒、西蓝花→西兰花);语气词和无关内容忽略(呃、啊、忘买饭了)。
- 规范名只保留会改变油量/热量的做法修饰(清炒、干锅、红烧、炸、水煮…);去掉不影响油热的修饰:"小炒"的"小"(小炒猪肝→炒猪肝)、温度(温牛奶→牛奶)、来源(自制拌面→拌面、那道/外卖)、形态(干锅土豆片→干锅土豆)。注意"清炒"要保留,它标识少油。
- 不要从口感或形容词推断食材品种:用户说"玉米,甜糯糯的",就记「玉米」,不要记「糯玉米」。
- 若用户没说清吃了什么具体的菜(如"随便扒拉了几口""简单垫了点"),l1 输出 ["无法解析"],l2、l3 都输出 {},整条不再解析。

# L2 份量量词(L1 的每道菜一个值)
- 用户说了份量 → 记量词,做轻归一化:
  - 补上省略的"一":碗→一碗、份→一份、个→一个。
  - 去掉比拟尾"大小""大":一拳大小→一拳、巴掌大→一掌。
  - 身体部位量统一用字:巴掌→掌、拳头→拳。例:巴掌大→一掌、两个半巴掌大→两个半掌、半个拳头大小→半拳。
- 用户没说份量 → "未提供"。绝不脑补,原句里没有量词就是"未提供"。
- 量词必须挂到正确的菜。

# L3 油量(L1 的每道菜一个值)
- 用户提到了油 → 记油量描述原文,保留语义、不归并不同程度(非常油、油汪汪、几乎没放油、没放油、一点油都没有、沥过油……都算"提到了",照记)。
- 关键:"声明没放油"(没放油、几乎没放油、沥过油)也算提到了油 → 照记原文,绝不写成"无"。
- 用户完全没提油 → "无"。不脑补。
- 油量描述必须挂到正确的菜。

# 示例(仅供格式参考)
输入:早上吃了一个豆沙包
输出:{"l1":["豆沙包"],"l2":{"豆沙包":"一个"},"l3":{"豆沙包":"无"}}
输入:晚饭米饭、宫保鸡丁、海带豆腐汤
输出:{"l1":["米饭","宫保鸡丁","海带豆腐汤"],"l2":{"米饭":"未提供","宫保鸡丁":"未提供","海带豆腐汤":"未提供"},"l3":{"米饭":"无","宫保鸡丁":"无","海带豆腐汤":"无"}}
输入:清炒西葫芦没放油,配一份糖醋排骨
输出:{"l1":["清炒西葫芦","糖醋排骨"],"l2":{"清炒西葫芦":"未提供","糖醋排骨":"一份"},"l3":{"清炒西葫芦":"没放油","糖醋排骨":"无"}}
输入:吃了巴掌大的煎牛排
输出:{"l1":["煎牛排"],"l2":{"煎牛排":"一掌"},"l3":{"煎牛排":"无"}}
输入:中午随便对付了一口
输出:{"l1":["无法解析"],"l2":{},"l3":{}}"""


def parse(input_text: str) -> dict:
    """调用 DeepSeek V4-Flash（非思考模式）解析,返回与真值同构的结构化结果。"""
    resp = client.chat.completions.create(
        model="deepseek-v4-flash",
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {"role": "user", "content": input_text},
        ],
        extra_body={"thinking": {"type": "disabled"}},
        response_format={"type": "json_object"},
        temperature=0,
    )
    raw = (resp.choices[0].message.content or "").strip()
    raw = raw.removeprefix("```json").removeprefix("```").removesuffix("```").strip()
    try:
        data = json.loads(raw)
        return {
            "l1": data.get("l1", []),
            "l2": data.get("l2", {}),
            "l3": data.get("l3", {}),
        }
    except json.JSONDecodeError:
        return {"l1": ["__PARSE_FAILED__"], "l2": {}, "l3": {}}


def normalize_dish(name: str) -> str:
    """对菜品名做同义词归一化（如存在映射）。"""
    return SYNONYM_MAP.get(name, name)
